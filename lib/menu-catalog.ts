export type MenuItemId = (typeof MENU_ITEM_CATALOG)[number]['id'];

export interface MenuCatalogEntry {
  id: MenuItemId;
  label: string;
  aliases: readonly string[];
}

export const INACTIVE_MENU_ITEM_IDS = new Set<MenuItemId>([
  'chiffon_mothers_day_tokachi_crepe',
]);

const normalizeMenuAlias = (value: string) => value.trim().replace(/\s+/g, ' ');

const MENU_ITEM_BY_ID = new Map<MenuItemId, MenuCatalogEntry>(
  MENU_ITEM_CATALOG.map((entry) => [entry.id, entry])
);

const MENU_ITEM_ID_BY_ALIAS = new Map<string, MenuItemId>();

for (const entry of MENU_ITEM_CATALOG) {
  MENU_ITEM_ID_BY_ALIAS.set(normalizeMenuAlias(entry.label), entry.id);
  for (const alias of entry.aliases) {
    MENU_ITEM_ID_BY_ALIAS.set(normalizeMenuAlias(alias), entry.id);
  }
}

export function resolveMenuItemId(name: string | null | undefined): MenuItemId | null {
  if (!name) return null;
  return MENU_ITEM_ID_BY_ALIAS.get(normalizeMenuAlias(name)) ?? null;
}

export function getMenuCatalogEntry(id: string | null | undefined): MenuCatalogEntry | null {
  if (!id) return null;
  return MENU_ITEM_BY_ID.get(id as MenuItemId) ?? null;
}
